			<h1>Comment appairer le DJI Mavic 2</h1>
<p>Les méthodes pour appairer le DJI Mavic Pro 2 sont les mêmes pour les autres drones de la gamme : Mavic 2 Zoom, Mavic 2 Enterprise et Mavic 2 Enterprise Dual.</p>
<p>Lorsque vous <a href="https://www.studiosport.fr/guides/mavic-2/recap/utilisation-mavic-2.php">utilisez un DJI Mavic Pro 2</a>, la connexion entre le drone et la radiocommande peut parfois être interrompue. Vous remarquerez alors à ce moment là que les LEDs situées sur l'arrière du drone deviennent jaunes et clignotent. Une inscription "Connecting..." peut également apparaître sur la radiocommande. <strong>L'appairage entre votre drone et la radiocommande doit être effectué de nouveau</strong>. 
<br>Il existe <strong>deux façons</strong> de ré-appairer votre radiocommande à votre drone Mavic 2 : <strong>depuis l'application</strong> ou bien <strong>grâce à une combinaison de touches</strong>.</p>
<h2>Appairer la radiocommande du DJI Mavic 2 Pro ou DJI Mavic 2 Zoom depuis DJI GO 4</h2>
<p>Pour se faire, vous devez allumer le drone ainsi que la radiocommande et lancer l'application DJI GO 4. Entrez en vue caméra. Vous allez activer la fonction d'appairage : touchez l'icône désignant trois petits points dans le coin supérieur droit. Touchez l'onglet radiocommande et activez la fonction "<em>Linking Remote Controller</em>" tout en bas (Effectuer l'appairage en français). Appuyez sur "OK", la radiocommande va commencer à <strong>biper</strong>, vous avez alors <strong>60 secondes</strong> pour pressez le bouton d'appairage au niveau du drone. Ce bouton se trouve sous le drone sur le côté gauche et nécessite d'ouvrir un petit cache en plastique. La LED tout près du bouton deviendra verte après l'avoir pressé, vous confirmant que l'appairage s'est correctement réalisé. Les LEDs arrières du drone vont devenir vertes et clignoter plus lentement. De même, un message vous confirmant la réussite de l'opération s'affichera sur votre application DJI GO 4.</p>
<h2>Appairer la radiocommande par une combinaison de touches</h2>
<p>Cette méthode héritée des anciens modèles de drones DJI, permet d'appairer la radiocommande au drone sans avoir besoin d'y connecter votre smartphone. Pour se faire, allumer le drone ainsi que la radiocommande comme dans la méthode précédente. Prenez la radiocommande en mains et pressez les boutons suivants en même temps : <strong>C1</strong>, <strong>C2</strong>, <strong>bouton Record</strong> puis sans relâcher, appuyez sur le <strong>bouton Shutter</strong>. La radiocommande va alors émettre un bip et vous indiquer sur l'écran que l'appairage est en cours ("<em>Binding</em>"), vous pouvez relâcher tous les boutons. Vous avez <strong>60 secondes</strong> pour pressez le bouton d'appairage situé sur le drone comme dans la méthode précédente. Une fois le bouton pressé , la LED juste à côté va devenir verte vous confirmant la réussite de l'opération. Une fois cela terminé, la radiocommande va arrêter de biper et les LEDs du drone vont devenir vertes.</p>
