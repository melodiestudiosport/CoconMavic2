			<h1>Quelle application utiliser avec le drone DJI Mavic Pro 2 ?</h1>
		<p>Il est possible d'utiliser <strong>différentes applications</strong> pour utiliser les drones DJI de la <srong>gamme Mavic 2</strong> (Pro et Zoom). Le <a href="https://www.studiosport.fr/guides/mavic-2/recap/choisir-mavic-2.php">Mavic 2 que vous allez choisir</a> et l'utilisation de ce dernier détermineront l'application qu'il convient d'utiliser. 
		Nous recommandons de toujours <strong>utiliser les applications développées par DJI</strong> et non des applications tierces. En effet, en cas de doute sur la responsabilité du pilote lors d'un crash, DJI pourra vous demander de mettre à sa disposition les données contenues dans leurs applications. Dans certains cas et sans ces données, l'analyse de la responsabilité ne pourra se faire.</p>
	<h2>L'application DJI GO 4</h2>
		<p>Cette application développée par DJI permet de <strong>piloter les drones DJI Mavic 2 Pro et DJI Mavic 2 Zoom</strong>. En effet, cette application existe depuis de nombreuses années chez DJI et prend en charge également d'autres produits de la marque (<em>Phantom</em>, <em>Inspire</em>, <em>Spark</em>, <em>Matrice</em> ou <em>Mavic Air</em> par exemple). L'application est très <strong>intuitive</strong> et <strong>simple à prendre en main</strong>. Il est important de noter que celle-ci n'est disponible qu'en Français sur les smartphones iOs. DJI GO 4 est disponible en Anglais sur les périphériques Android. 
		DJI GO 4 devrait reconnaître le drone une fois l'application lancée (et si le drone et la radiocommande sont allumés). Si ce n'est pas le cas, vous avez la possibilité d'<strong>appairer le drone</strong> depuis l'application en entrant en Vue Caméra. Rendez-vous dans l'onglet "Radiocommande" et cliquer sur "Appairer la radiocommande". La radio va commencer à biper, il faut alors presser le bouton d'appairage du drone dans les 60 secondes. Une fois la connexion établie, le retour vidéo du drone va s'afficher sur votre smartphone.
		DJI GO 4 permet d'effectuer <strong>nombres d'actions</strong> en plus du visionnage du <strong>retour vidéo de votre drone</strong>. La navigation est aisée puisque très intuitive, des icônes simples aident à la compréhension des actions.</p>
	<h2>L'application DJI Pilot</h2>
		<p>L'application DJI Pilot est quant à elle, réservée aux drones DJI Mavic 2 de la <strong>gamme Enterprise</strong> : le <strong>DJI Mavic 2 Enterprise</strong> et le <strong>DJI Mavic 2 Enterprise Dual</strong>.
		DJI Pilot est d'autant plus réservée à une <strong>utilisation professionnelle et métier</strong> : les drones <em>Enterprise</em> sont très utilisés par les professionnels du secourisme (pompiers), de la sécurisation de sites sensibles (centrales nucléaires) etc...
		L'application reprend les fonctions de base de DJI GO 4 à savoir le pilotage et les principaux modes de vol mais permet également la <strong>planification de vols programmés</strong> et nombre d'autres fonctions.</p>
	<h2>Les fonctionnalités communes</h2>
		<p>Les deux applications nécessitent d'<strong>avoir un compte DJI</strong> qu'il est possible de créer directement depuis votre smartphone ou sur le site de DJI.
		Quelques fonctions communes aux deux applications viennent ajouter du <strong>dynamisme dans la réalisation de séquences photos/vidéos</strong>. Les nombreux modes de vols intelligents peuvent par exemple permettre de réaliser des plans dynamiques (Dolly Zoom, panorama etc...).
		Le contrôle du mode de vol est accessible depuis DJI GO 4 et DJI Pilot, permettant de passer d'un mode Sport à un mode Trépied par exemple.
		<br><br>Nous recommandons l'utilisation de ces deux applications selon le modèle de votre drone. Il est important de rappeler que ces deux applications permettent d'utiliser correctement votre drone et s'assurer qu'il dispose toujours des dernières mises à jour. En utiliser d'autres peut vous permettre d'acquérir une nouvelle expérience de vol mais ne peuvent entièrement remplacer DJI GO 4 et DJI Pilot.</p>
	<h2>L'alternative de l'application Litchi</h2>
		<p>Il existe également une autre application semblable à DJI GO 4 pour piloter les drones Mavic 2 : Litchi. L'application est payante sur les plateformes Google Play et App Store et peut compléter l'utilisation de DJI GO 4. Litchi reprend les fonctions de base de DJI GO 4 et met d'avantage en avant la possibilité de planifier des missions de vol personnalisables. Litchi est disponible en Français sur tous les appareils, ce qui ajoute un réel plus comparé à DJI GO 4 (uniquement traduite sur iOS).</p>
	<h2>La photogrammétrie grâce à Pix4D Capture</h2>
		<p>Pix4D Capture: Cette application permet de réaliser une série de photos en vue de modéliser en 3D un espace défini. Pix4D Capture vous permet de planifier une mission que le drone remplira par la suite. Une fois la série de photos réalisées par le drone, vous pouvez les traiter à l'aide d'un logiciel de mapping tel que Pix4D Mapper, Ground Station ou encore DroneDeploy. Cette application est d'autant plus réservée à la pratique de la photogrammétrie.</p>

